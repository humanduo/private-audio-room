function safeFileName(value: string) {
  return value.replace(/[^a-zA-Z0-9_-]/g, '_').slice(0, 80);
}

function stableId(value: string, length = 18) {
  return crypto.createHash('sha1').update(value).digest('base64url').slice(0, length);
}

