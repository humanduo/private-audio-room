app.get('/media/:albumId/:episodeId', (req, res) => {
  const state = readState();
  const album = state.albums.find((item) => item.id === req.params.albumId);
  const episode = album?.episodes.find((item) => item.id === req.params.episodeId);
  if (!episode?.filePath) return res.status(404).json({ error: 'Media file not found' });

  const resolved = path.resolve(episode.filePath);
  const root = path.resolve(state.nas.root || process.env.AUDIO_ROOT || path.dirname(resolved));
  if (!isPathInsideRoot(resolved, root) || !fs.existsSync(resolved)) {
    return res.status(403).json({ error: 'Media path is not allowed' });
  }

  const stat = fs.statSync(resolved);
  const fileSize = stat.size;
  const range = req.headers.range;
  const contentType = audioContentType(resolved);

  if (!range) {
    res.writeHead(200, {
      'Accept-Ranges': 'bytes',
      'Content-Length': fileSize,
      'Content-Type': contentType
    });
    fs.createReadStream(resolved).pipe(res);
    return;
  }

  const match = range.match(/bytes=(\d*)-(\d*)/);
  if (!match) return res.status(416).json({ error: 'Invalid range' });

  const start = match[1] ? Number(match[1]) : match[2] ? Math.max(fileSize - Number(match[2]), 0) : 0;
  const end = match[1] ? (match[2] ? Number(match[2]) : fileSize - 1) : fileSize - 1;
  if (start >= fileSize || end >= fileSize || start > end) {
    res.status(416).set('Content-Range', `bytes */${fileSize}`).end();
    return;
  }

  res.writeHead(206, {
    'Accept-Ranges': 'bytes',
    'Content-Range': `bytes ${start}-${end}/${fileSize}`,
    'Content-Length': end - start + 1,
    'Content-Type': contentType
  });
  fs.createReadStream(resolved, { start, end }).pipe(res);
});

